package com.javase.ejemplo.herencia.carro;

public class Llantas 
{
	private String clima;
	private int radio;
	public Llantas()
	{
		
	}
	public void setRadio(int vel)
	{
		radio=vel;
	}
	public int getRadio()
	{
		return radio;
	}
	public void setClima(String wheel)
	{
		clima=wheel;
	}
	public String getClima()
	{
		return clima;
	}
}
